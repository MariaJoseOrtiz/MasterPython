numero = int(input("Introduce un numero: "))

numero2 = int(input("Introduce el otro numero: "))

print("### Calculadora ##")

print("Suma: " + str(numero+numero2) )
print("Resta: " + str(numero-numero2) )
print("Multi: " + str(numero*numero2) )
print("Dividir: " + str(numero/numero2) )